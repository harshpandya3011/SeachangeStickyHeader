package com.seachange.healthandsafty.db;

import androidx.lifecycle.LiveData;
import androidx.room.Dao;
import androidx.room.Delete;
import androidx.room.Insert;
import androidx.room.Query;
import androidx.room.Update;

import java.util.List;

@Dao
public interface  DBTourCheckDao {

    // LiveData is a data holder class that can be observed within a given lifecycle.
    // Always holds/caches latest version of data. Notifies its active observers when the
    // data has changed. Since we are getting all the contents of the database,
    // we are notified whenever any of the database contents have changed.
    @Query("SELECT * from dbtourcheck")
    LiveData<List<DBTourCheck>> getAllTourChecks();

    // We do not need a conflict strategy, because the word is our primary key, and you cannot
    // add two items with the same primary key to the database. If the table has more than one
    // column, you can use @Insert(onConflict = OnConflictStrategy.REPLACE) to update a row.
    @Insert
    void insert(DBTourCheck check);

    @Update
    void update(DBTourCheck check);

    @Delete
    void delete(DBTourCheck check);

    @Query("DELETE FROM dbtourcheck")
    void deleteAll();
//}

}
